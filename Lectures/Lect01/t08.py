# Написати програму, що визначається значення поліному
# 𝑦=𝑥^6 – 4𝑥^4  + 3𝑥 – 7
# у введеній з клавіатури точці 𝑥

x = float(input("x = ?"))
y = x ** 6 - 4 * x ** 4 - 3 * x - 7
# print("y", "(", x, ")= ", y)  #  y ( 4.0 )= y
print(f"y({x}) = {y}")   # y(4.0) = 3053.0
